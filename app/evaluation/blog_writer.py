# generates a technical blog post analyzing the findings.
import os
import json
from datetime import datetime
from dotenv import load_dotenv
from langchain_groq import ChatGroq
from langchain_core.messages import HumanMessage, SystemMessage
from app.models import BenchmarkComparision, RunResult
load_dotenv()

def get_llm() -> ChatGroq:
    return ChatGroq(
        model=os.getenv("AGENT_MODEL", "llama-3.1-8b-instant"),
        temperature=0.4,
        api_key=os.getenv("GROQ_API_KEY"),
        max_tokens=1024
    )

def format_comparison_table(comparison: BenchmarkComparision) -> str: #Builds a markdown table from the benchmark results. 
    lines = []
    lines.append(f"## Benchmark Results: {comparison.topic}")
    lines.append("")
    lines.append("| Agent | Latency (s) | Tokens | Cost (USD) | Quality Score |")
    lines.append("|-------|-------------|--------|------------|---------------|")
    for result in comparison.results:
        quality = f"{result.quality_score:.1f}/10" if result.quality_score else "N/A"
        cost = f"{result.token_usage.estimated_cost_usd:.6f}"
        lines.append(
            f"| {result.agent_type.value:<12} "
            f"| {result.latency_seconds:<11.2f} "
            f"| {result.token_usage.total_tokens:<6} "
            f"| {cost:<10} "
            f"| {quality:<13} |"
        )
    lines.append("")
    lines.append(f"**Fastest agent:** {comparison.fastest_agent.value if comparison.fastest_agent else 'N/A'}")
    lines.append(f"**Cheapest agent:** {comparison.cheapest_agent.value if comparison.cheapest_agent else 'N/A'}")
    lines.append(f"**Highest quality:** {comparison.highest_quality_agent.value if comparison.highest_quality_agent else 'N/A'}")
    return "\n".join(lines)

def format_agent_summaries(comparison: BenchmarkComparision) -> str:
    sections = []
    for result in comparison.results:
        section = f"""
        ### {result.agent_type.value.upper()} Agent
        - Latency: {result.latency_seconds}s
        - Tokens: {result.token_usage.total_tokens}
        - Quality Score: {result.quality_score}/10
        - Feedback: {result.quality_feedback}
        - Report preview: {result.report[:300]}...
        """
        sections.append(section)
    return "\n".join(sections)

def generate_blog_post(comparison: BenchmarkComparision) -> str:
    table = format_comparison_table(comparison)
    summaries = format_agent_summaries(comparison)
    llm = get_llm()
    system_msg = SystemMessage(content=(
        "You are a senior ML engineer writing a technical blog post. "
        "Write in a clear, professional but engaging style. "
        "Be specific about numbers and tradeoffs. "
        "Use markdown formatting with headers and bullet points. "
        "The audience is software engineers who want to understand "
        "when to use each agent architecture."
    ))

    human_msg = HumanMessage(content=f"""
        Write a technical blog post comparing four AI agent architectures.
        Use the benchmark data below to make specific, data-driven points.

        Topic researched: {comparison.topic}

        {table}

        {summaries}

        Structure the blog post with these sections:
        1. Introduction - why comparing agent architectures matters
        2. The Four Architectures - brief description of each
        3. Results Analysis - what the numbers show, be specific
        4. When to Use Each - practical decision guide
        5. Conclusion - key takeaways

        Keep the total post under 800 words.
        Write in markdown format starting with a # title.
        """)
    
    try:
        response = llm.invoke([system_msg, human_msg])
        blog_content = response.content
    except Exception as e:
        blog_content = f"Blog generation failed: {str(e)}\n\n{table}"

    
    full_post = f"""{blog_content}

        ---
        *Generated by agent-benchmark on {datetime.now().strftime('%Y-%m-%d')}*
        *Models used: {os.getenv('AGENT_MODEL', 'llama-3.1-8b-instant')}*
        """
    return full_post


def save_blog_post(content: str, topic: str) -> str:
    os.makedirs("outputs", exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    safe_topic = topic.replace(" ", "_")[:30]
    filename = f"outputs/blog_{safe_topic}_{timestamp}.md"
    with open(filename, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"[Blog] Saved to {filename}")
    return filename